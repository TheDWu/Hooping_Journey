import turtle
s=turtle.Screen()
t=turtle.Turtle()


def draw_court():
#rectangle
    t.penup()
    t.goto(-200, -250)
    t.pendown()
    t.forward(400)
    t.setheading(90)
    t.forward(500)
    t.setheading(180)
    t.forward(400)
    t.setheading(270)
    t.forward(500)
    t.setheading(360)

#halfcourt line
    t.penup()
    t.goto(-200, 0)
    t.pendown()
    t.setheading(360)
    t.forward(400)

#halfcourt circle
    t.penup()
    t.goto(0,-40)
    t.pendown()
    t.circle(40)

#3-pt lines
    t.penup()
    t.goto(-130, -250)
    t.pendown()
    t.setheading(90)
    t.forward(20)
    t.circle(-130, 180)
    t.setheading(270)
    t.forward(20)
    t.penup()
    t.goto(-130, 250)
    t.pendown()
    t.setheading(270)
    t.forward(20)
    t.circle(130, 180)
    t.setheading(90)
    t.forward(20)

#nets
    t.penup()
    t.goto(0, -240)
    t.pendown()
    t.setheading(180)
    t.forward(20)
    t.setheading(360)
    t.forward(40)
    t.goto(0,-240)
    t.setheading(90)
    t.forward(5)
    t.setheading(360)
    t.circle(5)

    t.penup()
    t.goto(0, 240)
    t.pendown()
    t.setheading(180)
    t.forward(20)
    t.setheading(360)
    t.forward(40)
    t.goto(0, 240)
    t.setheading(270)
    t.forward(5)
    t.setheading(360)
    t.circle(-5)

#free throw and baseline
    t.penup()
    t.goto(-50, -250)
    t.pendown()
    t.setheading(90)
    t.forward(110)
    t.setheading(360)
    t.forward(100)
    t.setheading(270)
    t.forward(110)
    t.setheading(180)
    t.forward(10)
    t.setheading(90)
    t.forward(110)
    t.setheading(180)
    t.forward(80)
    t.setheading(270)
    t.forward(110)
    t.setheading(90)
    t.forward(110)
    t.circle(-40, 180)

    t.penup()
    t.goto(-50, 250)
    t.pendown()
    t.setheading(270)
    t.forward(110)
    t.setheading(360)
    t.forward(100)
    t.setheading(90)
    t.forward(110)
    t.setheading(180)
    t.forward(10)
    t.setheading(270)
    t.forward(110)
    t.setheading(180)
    t.forward(80)
    t.setheading(90)
    t.forward(110)
    t.setheading(270)
    t.forward(110)
    t.circle(40, 180)

#restricted area
    t.penup()
    t.goto(-24, -235)
    t.pendown()
    t.circle(-24, 180)

    t.penup()
    t.goto(-24, 235)
    t.pendown()
    t.circle(24, 180)
    t.pendown()

draw_court()